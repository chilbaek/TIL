SELECT DEPTNO, JOB,
AVG(SAL*12*TO_NUMBER(TO_CHAR(HIREDATE,'MM')/10+1)) AS NEW,
AVG(
    CASE TO_CHAR(HIREDATE, 'MM')
        WHEN '01' THEN SAL*1.1*12
        WHEN '02' THEN SAL*1.2*12
        WHEN '03' THEN SAL*1.3*12
        WHEN '04' THEN SAL*1.4*12
        WHEN '05' THEN SAL*1.5*12
        WHEN '06' THEN SAL*1.6*12
        WHEN '07' THEN SAL*1.7*12
        WHEN '08' THEN SAL*1.8*12
        WHEN '09' THEN SAL*1.9*12
        WHEN '10' THEN SAL*2.0*12
        WHEN '11' THEN SAL*2.1*12
        WHEN '12' THEN SAL*2.2*12
    END
) AS NEW_AVG
FROM EMP
GROUP BY DEPTNO, JOB
ORDER BY DEPTNO;